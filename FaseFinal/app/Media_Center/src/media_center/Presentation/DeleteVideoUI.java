    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // DELETE SELECTED BUTTON
        int id = (Integer)jTable1.getValueAt(jTable1.getSelectedRow(), 2);
        
        VideoDAO vdao = new VideoDAO();
        Video vid = vdao.get(id);
        String path = vid.getPath();
        
        ProcessBuilder pb = new ProcessBuilder("rm", path);
        try {
            pb.start();
        } catch (IOException ex) {
            Logger.getLogger(DeleteVideoUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        vdao.remove(vid.getId());
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedIndex = jTable1.getSelectedRow();
        if (selectedIndex != -1) {
            model.removeRow(selectedIndex);
        }

        JOptionPane.showMessageDialog(this, "Deleted Video.");
    }//GEN-LAST:event_jButton1ActionPerformed

