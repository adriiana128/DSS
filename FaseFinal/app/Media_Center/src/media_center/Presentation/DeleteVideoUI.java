    public void buildTable(){
        User user = del_UI.getUser();
        VideoDAO vdao = new VideoDAO();
        ArrayList<Video> allvideos = new ArrayList<>(vdao.values());
        
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
            return false;
            }
        };
        
        model.addColumn("Title");
        model.addColumn("Owner");
        model.addColumn("id");
        
        for(Video v : allvideos) {
            if(v.getDono().equals(user.getUsername())){
                Object[] row = {v.getNome(), v.getDono(), v.getId()};
                model.addRow(row);
            }
        }
        
        jTable1.setModel(model);
        jTable1.getColumnModel().getColumn(2).setMaxWidth(25);
        jTable1.getTableHeader().setReorderingAllowed(false);
    }
    
